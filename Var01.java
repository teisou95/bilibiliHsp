//变量定义
//三个基本要素
//类型 名称 值
//声明变量--赋值/也可以一步到位，通常是一步完成
//变量相当于内存中一个数据存储空间的表示
//通过变量名可以访问到变量（值）


public class Var01 {
	//编写main主方法
	public static void main(String[] args) {

		//声明变量
		int a;
		//赋值
		a = 100;
		//输出
		System.out.println(a);

		//一步到位
		int b = 200;
		System.out.println(b);



		// //定义变量
		// int a = 1;	//类型int 变量名a 值1
		// int b = 3;	//类型int 变量名b 值3
		// b = 89;		//把89的值赋给b

		// //输出语句
		// System.out.println("a = " + a);
		// System.out.println("b = " + b);
	}
}