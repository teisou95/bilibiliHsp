//变量使用入门案例
//演示记录人的信息的代码

public class Var02 {
	//编写main主方法
	public static void main(String[] args) {

		//记录人的信息
		//定义变量
		int age = 20;
		double score = 88.6;
		char gender = '男';
		String name = "jack";

		//输出信息
		System.out.println("人的信息如下：");
		System.out.println(name);
		System.out.println(age);
		System.out.println(score);
		System.out.println(gender);

	}
}